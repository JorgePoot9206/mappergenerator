// ── Utilities ────────────────────────────────────────────────

function escapeHtml(str: string): string {
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

/** Convert a ZoneShape from % coords to pixel coords string for <area coords="…"> */
function shapeToCoords(shape: ZoneShape, W: number, H: number): string {
  const px = (v: number, dim: number) => Math.round((v / 100) * dim);
  if (shape.type === "rect") {
    const x1 = px(shape.x, W);
    const y1 = px(shape.y, H);
    const x2 = px(shape.x + shape.w, W);
    const y2 = px(shape.y + shape.h, H);
    return `${x1},${y1},${x2},${y2}`;
  }
  if (shape.type === "circle") {
    return `${px(shape.cx, W)},${px(shape.cy, H)},${px(shape.r, Math.min(W, H))}`;
  }
  if (shape.type === "poly") {
    return shape.points.map((p) => `${px(p.x, W)},${px(p.y, H)}`).join(",");
  }
  return "";
}
